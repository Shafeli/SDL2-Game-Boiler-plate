#pragma once


/////////////////////////////////////////////////
// State interface
/////////////////////////////////////////////////


class State
{
public:
    /////////////////////////////////////////////////
    // must override state methods
    /////////////////////////////////////////////////
    virtual ~State() = default;
    virtual void Init() = 0;
    virtual void HandleInput() = 0;
    virtual void Update(float dt) = 0;
    virtual void Draw() = 0;

    /////////////////////////////////////////////////
    // optional state methods
    /////////////////////////////////////////////////
    virtual void Pause() {}
    virtual void Resume() {}

};
